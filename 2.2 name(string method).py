# the title method can be used to capitalize the first letter of each word in a string
name1 = 'ada wang'
print(name1.title())
name2 = 'AdA wANg'
print(name2.title())

# upper method make all letters in a string uppercase
name3 = 'TOmmy LOOng'
print(name3.upper())

# lower method make all letters in a string lowercase
name4 = 'ToMMY loONG'
print(name4.lower())

# usually the lower method is used when comparing strings, because it ignores case differences